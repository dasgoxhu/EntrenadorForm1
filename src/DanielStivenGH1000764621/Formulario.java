    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Equipo jugador;
        jugador = new Jugador
               (Integer.parseInt(JOptionPane.showInputDialog(rootPane, "¿ingrese dorsal?")), 
                JOptionPane.showInputDialog(rootPane, "¿Ingrese demarcacion?"),
                JOptionPane.showInputDialog(rootPane, "¿ingrese el nombre?"), 
                JOptionPane.showInputDialog(rootPane, "¿ingrese el Apellido?"),       
                Integer.parseInt(JOptionPane.showInputDialog(rootPane, "¿ingrese el Id?")), 
                Integer.parseInt(JOptionPane.showInputDialog(rootPane, "¿ingrese la edad?")));
        ListEquipo.add(contador,jugador.imprimir());
        jFrame1.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

